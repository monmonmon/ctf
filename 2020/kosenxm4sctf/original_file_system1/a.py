#!/usr/bin/env python

p = 'xm4s!'.encode()
l = [0x5a, 0x01, 0x02, 0x41, 0x2c, 0x47, 0x11, 0x03, 0x50, 0x2c, 0x40, 0x27, 0x0b, 0x5d, 0x1f, 0x44, 0x27, 0x03, 0x55, 0x1e, 0x44, 0x59, 0x10]
p_len = len(p)

for i in range(5):
    print('i =', i)
    for c in l:
        print(chr(c ^ p[i]), end='')
        i = (i + 1) % p_len
    print()
